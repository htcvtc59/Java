    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try {
            Properties props = System.getProperties();
            props.setProperty("mail.host", "MacOS");
            props.setProperty("port", "25");
            Session ss = Session.getDefaultInstance(props);
            MimeMessage msg = new MimeMessage(ss);
            msg.setFrom(new InternetAddress(txtFrom.getText()));
            msg.setRecipient(RecipientType.TO, new InternetAddress(txtTo.getText()));
            msg.setSubject(txtSubject.getText());
            msg.setContent(txtAContent.getText(), "text/plain");
            Transport.send(msg);
            
        } catch (Exception ex) {
            Logger.getLogger(SendMail.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSendActionPerformed

