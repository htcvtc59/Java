    private void MenuItemOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuItemOpenActionPerformed
        // TODO add your handling code here:
         BufferedReader br =null;
        
        int result = FileChooserOpen.showOpenDialog(jPanel1);
        if(result == FileChooserOpen.APPROVE_OPTION){
            try {
                currentFile= FileChooserOpen.getSelectedFile().getAbsolutePath();
                br =new BufferedReader(new FileReader(currentFile));
                
                
                String code="";
                String line="";
                while (true) {
                    try {
                        line=br.readLine();
                    } catch (IOException ex) {
                        Logger.getLogger(TabPanevsEditorPane.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    if(line==null){
                        break;
                    }
                    code+=line+"\n";
                }

                try {
                    br.close();
                } catch (IOException ex) {
                    Logger.getLogger(TabPanevsEditorPane.class.getName()).log(Level.SEVERE, null, ex);
                }
                edpCode.setText(code);
                edpPreview.setText(code);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(TabPanevsEditorPane.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_MenuItemOpenActionPerformed

