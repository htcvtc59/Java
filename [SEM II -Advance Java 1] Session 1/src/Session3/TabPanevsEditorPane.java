    private void MenuItemSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuItemSaveActionPerformed
        // TODO add your handling code here:
        if (currentFile == "") {
          int result =  FileChooserSave.showSaveDialog(viewMenu);
            if (result == FileChooserSave.APPROVE_OPTION) {
                currentFile = FileChooserSave.getSelectedFile().getAbsolutePath();
            }
        }
        FileWriter  fw ;
        try {
            fw = new FileWriter(currentFile);
            fw.write(edpCode.getText());
            fw.flush();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(TabPanevsEditorPane.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_MenuItemSaveActionPerformed

