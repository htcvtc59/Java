    private void cbbInterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbInterActionPerformed
        Locale lcl = new Locale("en", "US");
        if (cbbInter.getSelectedItem().toString().equals("VietNamese")) {
            lcl = new Locale("vi", "VN");
        }
        String language = lcl.getDisplayLanguage();
        String country = lcl.getDisplayCountry();
        lbCountry.setText(country);
        lbLanguage.setText(language);
        ResourceBundle rb = ResourceBundle.getBundle("Session3\\International\\Messages", lcl);
        String hello = rb.getString("hello");
        lbinter.setText(hello);
        lbinquiry.setText(rb.getString("inquiry"));
//        Tien te so
        NumberFormat nf = NumberFormat.getCurrencyInstance(lcl);
        String money = nf.format(100.00);
        fortfLang.setText(money);
//        Ngay th√°ng
        Date date = new Date();
        DateFormat df = DateFormat.getDateInstance(DateFormat.MEDIUM, lcl);
        String dateday = df.format(date);
        fortfDate.setText(dateday);
//        Message
        String message = rb.getString("message");
        Random rd = new Random();
        int vr = rd.nextInt(100);
        Date datevirus = new Date();
        MessageFormat mf = new MessageFormat(message, lcl);
        String virus = mf.format(message, datevirus, vr);
        lbMessageVirus.setText(virus);

    }//GEN-LAST:event_cbbInterActionPerformed

