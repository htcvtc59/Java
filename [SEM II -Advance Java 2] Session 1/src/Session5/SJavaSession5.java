    private void ListStudentValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ListStudentValueChanged
        try {
            int selectedIndex = ListStudent.getSelectedIndex();
            int selectedRow = selectedIndex + 1;
            if (rs.absolute(selectedRow)) {
                String name = rs.getString("Name");
                String address = rs.getString("Address");
                String dob = rs.getString("Date");
                txtAreaInfo.setText(name + "\n");
                txtAreaInfo.append("Address:\t" + address + "\n");
                txtAreaInfo.append("Date of birth:\t" + dob + "\n");

            }
        } catch (SQLException ex) {
            Logger.getLogger(SJavaSession5.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_ListStudentValueChanged

