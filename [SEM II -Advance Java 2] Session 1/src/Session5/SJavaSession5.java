    private void populateList() {
        dflm.removeAllElements();
        try {
            rs = DBUtil.getResultSet("select * from Student");
            while (rs.next()) {
                dflm.addElement(rs.getString("Name"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(SJavaSession5.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

