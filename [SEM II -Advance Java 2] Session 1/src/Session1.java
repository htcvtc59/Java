    public void SetupSQL() {
        
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;instanceName=SQLEXPRESS;databaseName=Demojdbc";
            con = DriverManager.getConnection(url, "sa", "230697");
            System.out.println("Connect success");
            
            String sql = "select * from Student";
            stmt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            
            rs = stmt.executeQuery(sql);
            rowSet = new JdbcRowSetImpl(rs);
            
            rowSet.addRowSetListener(new RowSetListener() {
                @Override
                public void rowSetChanged(RowSetEvent event) {
                    System.out.println("row set changed");
                }
                
                @Override
                public void rowChanged(RowSetEvent event) {
                    System.out.println("row changed");
                }
                
                @Override
                public void cursorMoved(RowSetEvent event) {
                    System.out.println("Curor moved");
                }
            });
            
            DatabaseMetaData dbmdt = con.getMetaData();
            System.out.println("SSE " + dbmdt.getSearchStringEscape());
            System.out.println("Driver Name " + dbmdt.getDriverName());
            
            ResultSetMetaData rsmd = rs.getMetaData();
            System.out.println(rsmd.getColumnCount());
            for (int i = 0; i < 4; i++) {
                System.out.println(rsmd.getColumnName(i + 1));
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Session1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
