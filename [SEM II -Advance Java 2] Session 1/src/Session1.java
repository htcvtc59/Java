    private void btnTransacsionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransacsionActionPerformed
        Savepoint sp = null;
        try {
            con.setAutoCommit(false);
            sp = con.setSavepoint("BeforeInsert");
            String sql1 = "insert into Student values('Mr.F','DSD',23)";
            String sql2 = "insert into Student values('Mr.G','GFC',17)";
            stmt.executeUpdate(sql1);
            stmt.executeUpdate(sql2);
            con.commit();
            con.setAutoCommit(true);
        } catch (SQLException ex) {
            try {
                con.rollback(sp);
                con.setAutoCommit(true);
            } catch (SQLException ex1) {
                Logger.getLogger(Session1.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
        

    }//GEN-LAST:event_btnTransacsionActionPerformed

