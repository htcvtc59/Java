    public ArrayList<DateOne> getArrayList() {
        ArrayList<DateOne> list = new ArrayList<>();
        try {
            String sql = "select * from today";
            stmt = conn.prepareStatement(sql);
            rs = stmt.executeQuery();
            while (rs.next()) {
                DateOne dateone = new DateOne(rs.getString("datetoday"));
                list.add(dateone);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return list;
    }

    public void showTable() {
        ArrayList<DateOne> list = getArrayList();
        DefaultTableModel model = (DefaultTableModel) tabledate.getModel();
        Object[] cols = new Object[1];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getDatetoday();
            model.addRow(cols);
        }
    }

    public void queryUpdate(String sql) {

        try {
            stmt = conn.prepareStatement(sql);
            if (stmt.executeUpdate() == 1) {
                DefaultTableModel model = (DefaultTableModel) tabledate.getModel();
                model.setRowCount(0);
                showTable();
            }
//            conn.close();
//            stmt.close();
//            rs.close();
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }

