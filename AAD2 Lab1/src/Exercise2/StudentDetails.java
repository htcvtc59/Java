    public ArrayList<Student> getArrayList() {
        ArrayList<Student> list = new ArrayList<>();
        try {
            String sql = "select * from student";
            stmt = conn.prepareStatement(sql);
            rs = stmt.executeQuery();
            while (rs.next()) {
                Student student = new Student(rs.getString("Name"), rs.getInt("RollNo"), rs.getString("Class"));
                list.add(student);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return list;
    }

    public void showTable() {
        ArrayList<Student> list = getArrayList();
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
        Object[] cols = new Object[3];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getName();
            cols[1] = list.get(i).getRollno();
            cols[2] = list.get(i).getClasss();
            model.addRow(cols);
        }
    }

    public void queryUpdate(String sql, String message) {

        try {
            stmt = conn.prepareStatement(sql);
            if (stmt.executeUpdate() == 1) {
                DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
                model.setRowCount(0);
                showTable();
                JOptionPane.showMessageDialog(null, "Record" + message + "!");
            }
//            conn.close();
//            stmt.close();
//            rs.close();
        } catch (SQLException se) {
            se.printStackTrace();
        }
    }

