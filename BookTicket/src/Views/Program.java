    public void setTblPhim() {
        ArrayList<Phim> data = crudPhim.search("");
        for (int i = 0; i < data.size(); i++) {
            Phim tmp = data.get(i);
            modelPhim.addRow(new Object[]{tmp.getPhim(), tmp.getTheloai(), tmp.getQuocgia(), tmp.getNsx(), tmp.getMota()});
        }
    }

    public void setTblLc() {
        ArrayList<LichChieu> data = crudLichChieu.search("");
        for (int i = 0; i < data.size(); i++) {
            LichChieu tmp = data.get(i);
            modelLichChieu.addRow(new Object[]{tmp.getPhim(), tmp.getPhong(), KhungGio[tmp.getGioPhat()], tmp.getNgay()});
        }
    }

    void setTblVe() {
        ArrayList<Ve> data = ticket.GetAll();
        for (Ve item : data) {
            modelVe.addRow(new Object[]{item.getId(), item.getPhim(), KhungGio[item.getGioPhat()], item.getDate(), item.getPhong(), item.getGhe(), String.valueOf(item.getGia()), item.getDt(), tranformStatus(item.getTrangthai())});
        }
    }

    public void setTblDonGia() {
        ArrayList<DonGia> data = crudDonGia.search("");
        for (DonGia item : data) {
            modelDonGia.addRow(new Object[]{item.getDate(), KhungGio[item.getSuat()], String.valueOf(item.getThuong()), String.valueOf(item.getVip())});
        }
    }

    public String tranformType_re(int i) {
        if (i == 1) {
            return "Vip";
        } else {
            return "Thường";
        }

    }

