    public int tranformStatus_Re(String s) {
        int x = -1;
        if (s.trim().equals("Đã Thanh Toán")) {
            x = 1;
            System.out.println("thanh toan");
        }
        if (s.equals("Chưa Thanh Toán")) {
            x = 0;
        }
        if (s.equals("Chưa Xuât Vé")) {
            x = 2;
        }
        if (s.equals("Cảnh Báo")) {
            x = 3;
        }
        if (s.equals("Quá Hạn")) {
            x = 4;
        }
        if (s.equals("Đã Hủy")) {
            x = 5;
        }
        if (s.equals("Hết Hạn")) {
            x = 6;
        }
        return x;
    }

    public int tranformKg(String s) {
        for (int i = 1; i <= 4; i++) {
            if (s.equals(KhungGio[i])) {
                return i;
            }
        }
        return 0;
    }

    public String tranformStatus(int i) {
        String s = null;
        if (i == 0) {
            s = "Chưa thanh toán";
        } else if (i == 1) {
            s = "Đã xuất vé";
        } else if (i == 2) {
            s = "Vé được xuất";
        } else if (i == 3) {
            s = "Cảnh Báo";
        } else if (i == 4) {
            s = "Quá Hạn";
        } else if (i == 5) {
            s = "Đã Hủy";
        } else if (i == 6) {
            s = "Hết Hạn";
        }

        return s;
    }

