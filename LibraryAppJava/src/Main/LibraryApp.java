    private void btnShowReportMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnShowReportMouseClicked
        email = new ArrayList();
        int ChoiceReport = cbbReportAll.getSelectedIndex();
        if (ChoiceReport == -1) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn báo cáo muốn xem!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }

        if (ChoiceReport == 0) {

            new TableReport().HanTraSach(TableReport);
            for (int i = 0; i < TableReport.getRowCount(); i++) {
                email.add(TableReport.getValueAt(i, 2).toString());
            }

        }
        if (ChoiceReport == 1) {

            if (txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null) {

                if (DateInFormat.isValidFormat(DateToDTime.Datechooser(txtPortDateStart)) == false) {
                    JOptionPane.showMessageDialog(null, "Nhập sai định dạng ngày", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else if (DateInFormat.isValidFormat(DateToDTime.Datechooser(txtPortDateFinish)) == false) {
                    JOptionPane.showMessageDialog(null, "Nhập sai định dạng ngày", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng điền khung ngày", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }

            if ((txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null) && txtPortReader.getText().length() == 0
                    && txtPortBook.getText().length() == 0) {
                new TableReport().SachDaMuon(TableReport, DateToDTime.Datechooser(txtPortDateStart), DateToDTime.Datechooser(txtPortDateFinish), null, null);

            } else if ((txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null) && txtPortReader.getText().length() != 0
                    && txtPortBook.getText().length() == 0) {
                new TableReport().SachDaMuon(TableReport, DateToDTime.Datechooser(txtPortDateStart), DateToDTime.Datechooser(txtPortDateFinish), null, txtPortReader.getText());

            } else if ((txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null) && txtPortReader.getText().length() == 0
                    && txtPortBook.getText().length() != 0) {
                new TableReport().SachDaMuon(TableReport, DateToDTime.Datechooser(txtPortDateStart), DateToDTime.Datechooser(txtPortDateFinish), txtPortBook.getText(), null);

            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() != 0 && (txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null)) {
                new TableReport().SachDaMuon(TableReport, DateToDTime.Datechooser(txtPortDateStart), DateToDTime.Datechooser(txtPortDateFinish), txtPortBook.getText(), txtPortReader.getText());

            }

        }
        if (ChoiceReport == 2) {
            if (txtPortBook.getText().length() == 0) {
                new TableReport().SachTrongKho(TableReport, null);
            } else if (txtPortBook.getText().length() != 0) {
                new TableReport().SachTrongKho(TableReport, txtPortBook.getText());
            }

        }
        if (ChoiceReport == 3) {
            if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() == 0) {
                new TableReport().SachQuaHan(TableReport, null, null);
                for (int i = 0; i < TableReport.getRowCount(); i++) {
                    email.add(TableReport.getValueAt(i, 7).toString());
                }
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() == 0) {
                new TableReport().SachQuaHan(TableReport, txtPortBook.getText(), null);
                for (int i = 0; i < TableReport.getRowCount(); i++) {
                    email.add(TableReport.getValueAt(i, 7).toString());
                }
            } else if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() != 0) {
                new TableReport().SachQuaHan(TableReport, null, txtPortReader.getText());
                for (int i = 0; i < TableReport.getRowCount(); i++) {
                    email.add(TableReport.getValueAt(i, 7).toString());
                }
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() != 0) {
                new TableReport().SachQuaHan(TableReport, txtPortBook.getText(), txtPortReader.getText());
                for (int i = 0; i < TableReport.getRowCount(); i++) {
                    email.add(TableReport.getValueAt(i, 7).toString());
                }
            }

        }
        if (ChoiceReport == 4) {

            if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() == 0) {
                new TableReport().SachBiMat(TableReport, null, null);
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() == 0) {
                new TableReport().SachBiMat(TableReport, txtPortBook.getText(), null);
            } else if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() != 0) {
                new TableReport().SachBiMat(TableReport, null, txtPortReader.getText());
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() != 0) {
                new TableReport().SachBiMat(TableReport, txtPortBook.getText(), txtPortReader.getText());

            }

        }
        if (ChoiceReport == 5) {
            if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() == 0) {
                new TableReport().DoanhThu(TableReport, null, null);
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() == 0) {
                new TableReport().DoanhThu(TableReport, txtPortBook.getText(), null);
            } else if (txtPortBook.getText().length() == 0 && txtPortReader.getText().length() != 0) {
                new TableReport().DoanhThu(TableReport, null, txtPortReader.getText());
            } else if (txtPortBook.getText().length() != 0 && txtPortReader.getText().length() != 0) {
                new TableReport().DoanhThu(TableReport, txtPortBook.getText(), txtPortReader.getText());
            }
        }

        if (ChoiceReport == 6) {
            if (txtPortDateStart.getDate() != null && txtPortDateFinish.getDate() != null
                    && txtPortBook.getText().length() != 0) {
                new TableReport().TuanSuatMuon(TableReport, DateToDTime.Datechooser(txtPortDateStart),
                        DateToDTime.Datechooser(txtPortDateFinish), txtPortBook.getText());

            } else {
                JOptionPane.showMessageDialog(null, "Điền thông tin ngày và mã sách");
            }

        }
        if (ChoiceReport == 7) {
            new TableReport().XepHangSach(TableReport);
        }


    }//GEN-LAST:event_btnShowReportMouseClicked

