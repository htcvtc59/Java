    private void btnPayBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPayBookMouseClicked
        if (number == 1) {
            if (txtDatePayBook.getDate() != null
                    && txtIdBook.getText().length() != 0
                    && txtIdReader.getText().length() != 0
                    && txtQuantityBook.getText().length() != 0) {
                if (reDAO.Update(new tblPayReader(Integer.parseInt(id), txtIdBook.getText(), txtIdReader.getText(),
                        Integer.parseInt(txtQuantityBook.getText()), DateToDTime.Datechooser(txtDatePayBook), txtMoneyBorrow.getText(),
                        txtMoneyOutDate.getText(), txtMoneyLost.getText()
                )) != null) {
                    JOptionPane.showMessageDialog(null, "Sửa phiếu trả thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    showTableReturn(TableReturn);
                    this.dispose();

                }

            } else {
                JOptionPane.showMessageDialog(null, "Sửa phiếu trả thất bại bạn không được để trống", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            if (txtDatePayBook.getDate() != null
                    && txtIdBook.getText().length() != 0
                    && txtIdReader.getText().length() != 0
                    && txtQuantityBook.getText().length() != 0) {
                if (reDAO.Insert(new tblPayReader(Integer.parseInt(id), txtIdBook.getText(), txtIdReader.getText(),
                        Integer.parseInt(txtQuantityBook.getText()), DateToDTime.Datechooser(txtDatePayBook), txtMoneyBorrow.getText(),
                        txtMoneyOutDate.getText(), txtMoneyLost.getText()
                )) != null) {
                    JOptionPane.showMessageDialog(null, "Thêm phiếu trả thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    txtIdBook.setText("");
                    txtMoneyBorrow.setText("");
                    txtMoneyLost.setText("");
                    txtMoneyOutDate.setText("");
                    //txtMoneyTotal.setText("");
                    txtQuantityBook.setText("");

                    txtIdBook.requestFocus();
                    showTableReturn(TableReturn);

                } else {
                    JOptionPane.showMessageDialog(null, "Thêm phiếu trả thất bại trùng mã tài liệu", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Bạn không được để trống", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnPayBookMouseClicked

