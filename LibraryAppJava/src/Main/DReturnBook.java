    private void txtQuantityBookKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQuantityBookKeyReleased
        if (Valid.ValidN(txtQuantityBook.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Giá trị không hợp lệ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            txtQuantityBook.requestFocus();
        } else if (txtIdReader.getText().length() != 0
                && txtIdBook.getText().length() != 0
                && txtQuantityBook.getText().length() != 0
                && txtDatePayBook.getDate() != null) {
            int mborrow = new MoneyBorrow().TienMuon(txtIdBook.getText(), Integer.parseInt(txtQuantityBook.getText()));
            int moutdate = new MoneyReturn().TienQuaHan(DateToDTime.Datechooser(txtDatePayBook),
                    txtIdBook.getText(), txtIdReader.getText(), Integer.parseInt(txtQuantityBook.getText()));
            txtMoneyBorrow.setText(String.valueOf(mborrow));
            txtMoneyOutDate.setText(String.valueOf(moutdate));
        }

    }//GEN-LAST:event_txtQuantityBookKeyReleased

