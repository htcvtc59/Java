    private void btnLostBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLostBookMouseClicked
        if (Valid.ValidN(txtQuantityBook.getText()) == false) {
            JOptionPane.showMessageDialog(null, "Giá trị không hợp lệ", "Thông Báo", JOptionPane.INFORMATION_MESSAGE);
            txtQuantityBook.requestFocus();
        } else if (txtIdReader.getText().length() != 0
                && txtIdBook.getText().length() != 0
                && txtQuantityBook.getText().length() != 0
                && txtDatePayBook.getDate() != null) {
            int mlost = new MoneyLost().TienMatSach(txtIdBook.getText(), Integer.parseInt(txtQuantityBook.getText()));
            txtMoneyLost.setText(String.valueOf(mlost));
            txtMoneyBorrow.setText("");
            txtMoneyOutDate.setText("");
        }

    }//GEN-LAST:event_btnLostBookMouseClicked

