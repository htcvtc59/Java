    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        try {
            //1. Base on password
            //2. Create Key based on keyspec by paswword
            //3. Create Salte
            //4. Parameter for decrypte/encrypte mode
            String password = txtPWD.getText();
            SecretKeyFactory skf = SecretKeyFactory.getInstance(ALGO);
            PBEKeySpec keySpec = new PBEKeySpec(password.toCharArray());
            SecretKey secretKey = skf.generateSecret(keySpec);
            SecureRandom random = SecureRandom.getInstance("SHA1PRNG");
            salte = random.generateSeed(8);
            PBEParameterSpec param = new PBEParameterSpec(salte, 8);
            Cipher cipher = Cipher.getInstance(ALGO);
            //Encrypt
            cipher.init(Cipher.ENCRYPT_MODE, secretKey, param);
            String clear = txtClear.getText();
            byte[] encrypted = cipher.doFinal(clear.getBytes());
            BASE64Encoder encoder = new BASE64Encoder();
            String encode = encoder.encode(encrypted);
            System.out.println("Encoded: " + encode);
            txtEnc.setText(encode);

        } catch (Exception ex) {
            Logger.getLogger(PasswordBasedGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

    private void btnDecrypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecrypActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            SecretKeyFactory skf = SecretKeyFactory.getInstance(ALGO);
            PBEKeySpec keySpec = new PBEKeySpec(txtPWD.getText().toCharArray());
            SecretKey secretKey = skf.generateSecret(keySpec);
            PBEParameterSpec param = new PBEParameterSpec(salte, 8);
            Cipher cipher = Cipher.getInstance(ALGO);
            //Encrypt
            cipher.init(Cipher.DECRYPT_MODE, secretKey, param);
            BASE64Decoder decoder = new BASE64Decoder();
            byte[] encypedByte = decoder.decodeBuffer(txtEnc.getText());
            System.out.println("|" + new String(encypedByte) + "|");
            //Giai ma
            byte[] decrypted = cipher.doFinal(encypedByte);
            txtDec.setText(new String(decrypted));
            System.out.println(new String(decrypted));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDecrypActionPerformed

