            String name = txtfname.getText();
            String address = DBUtil.encryptt(txtfaddress.getText());
            int age = Integer.parseInt(txtfage.getText());
            String sql = "insert into Student values('" + name + "','" + address + "'," + age + ")";
            int result = DBUtil.exeQuery(sql);
