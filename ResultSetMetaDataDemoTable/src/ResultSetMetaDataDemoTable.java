    public void Displaytable() {
        try {
            String sql = "select * from Student";
            rs = DBUtil.getResultSet(sql);
            rsmd = rs.getMetaData();
            int columns = rsmd.getColumnCount();
            dtm = new DefaultTableModel();
            Vector columns_name = new Vector();
            Vector data_row = new Vector();
            for (int i = 1; i <= columns; i++) {
                columns_name.addElement(rsmd.getColumnName(i));

            }
            dtm.setColumnIdentifiers(columns_name);
            while (rs.next()) {
                data_row = new Vector();
                for (int j = 1; j <= columns; j++) {
                    data_row.addElement(rs.getString(j));
                }
                dtm.addRow(data_row);
            }
            tableStudent.setModel(dtm);

        } catch (SQLException ex) {
            Logger.getLogger(ResultSetMetaDataDemoTable.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

